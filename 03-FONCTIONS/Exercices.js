// Fonction de soustraction
function func_soustraction(a, b) {
    return a - b;
}
var soustraction = func_soustraction(7, 2);
console.log(soustraction);

// Fonction de division
function func_division(a, b) {
    return a / b;
}
var division = func_division (10, 2);
console.log(division);

// Fonction de multiplication
function func_multiplication(a, b) {
    return a + b;
}
var multiplication = func_multiplication (3, 2);
console.log(multiplication);

// Fonction de pourcentage
function func_pourcentage(a, b) {
    return a / 100 * b;
}
var pourcentage = func_pourcentage (5, 10);
console.log(pourcentage);

